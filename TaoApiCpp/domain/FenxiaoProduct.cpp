void FenxiaoProduct::parseResponse() {
  if (responseParser->hasName("alarm_number")) {
  alarmNumber = responseParser->getNumByName("alarm_number");
  }
  if (responseParser->hasName("category_id")) {
  categoryId = responseParser->getStrByName("category_id");
  }
  if (responseParser->hasName("city")) {
  city = responseParser->getStrByName("city");
  }
  if (responseParser->hasName("cost_price")) {
  costPrice = responseParser->getStrByName("cost_price");
  }
  if (responseParser->hasName("created")) {
  created = responseParser->getDateByName("created");
  }
  if (responseParser->hasName("dealer_cost_price")) {
  dealerCostPrice = responseParser->getStrByName("dealer_cost_price");
  }
  if (responseParser->hasName("desc_path")) {
  descPath = responseParser->getStrByName("desc_path");
  }
  if (responseParser->hasName("description")) {
  description = responseParser->getStrByName("description");
  }
  if (responseParser->hasName("discount_id")) {
  discountId = responseParser->getNumByName("discount_id");
  }
  if (responseParser->hasName("have_guarantee")) {
  haveGuarantee = responseParser->getBoolByName("have_guarantee");
  }
  if (responseParser->hasName("have_invoice")) {
  haveInvoice = responseParser->getBoolByName("have_invoice");
  }
  if (responseParser->hasName("input_properties")) {
  inputProperties = responseParser->getStrByName("input_properties");
  }
  if (responseParser->hasName("is_authz")) {
  isAuthz = responseParser->getStrByName("is_authz");
  }
  if (responseParser->hasName("item_id")) {
  itemId = responseParser->getNumByName("item_id");
  }
  if (responseParser->hasName("items_count")) {
  itemsCount = responseParser->getNumByName("items_count");
  }
  if (responseParser->hasName("modified")) {
  modified = responseParser->getDateByName("modified");
  }
  if (responseParser->hasName("name")) {
  name = responseParser->getStrByName("name");
  }
  if (responseParser->hasName("orders_count")) {
  ordersCount = responseParser->getNumByName("orders_count");
  }
  if (responseParser->hasName("outer_id")) {
  outerId = responseParser->getStrByName("outer_id");
  }
  if (responseParser->hasName("pdus")) {
  QList<Parser *> listParser = responseParser->getListObjectParser("pdus");
  Parser *parser;
  foreach (parser, listParser) {
    FenxiaoPdu tmp;
    tmp.setParser(parser);
    tmp.parseResponse();
    pdus.append(tmp);
  }
  }
  if (responseParser->hasName("pictures")) {
  pictures = responseParser->getStrByName("pictures");
  }
  if (responseParser->hasName("pid")) {
  pid = responseParser->getNumByName("pid");
  }
  if (responseParser->hasName("postage_ems")) {
  postageEms = responseParser->getStrByName("postage_ems");
  }
  if (responseParser->hasName("postage_fast")) {
  postageFast = responseParser->getStrByName("postage_fast");
  }
  if (responseParser->hasName("postage_id")) {
  postageId = responseParser->getNumByName("postage_id");
  }
  if (responseParser->hasName("postage_ordinary")) {
  postageOrdinary = responseParser->getStrByName("postage_ordinary");
  }
  if (responseParser->hasName("postage_type")) {
  postageType = responseParser->getStrByName("postage_type");
  }
  if (responseParser->hasName("productcat_id")) {
  productcatId = responseParser->getNumByName("productcat_id");
  }
  if (responseParser->hasName("properties")) {
  properties = responseParser->getStrByName("properties");
  }
  if (responseParser->hasName("property_alias")) {
  propertyAlias = responseParser->getStrByName("property_alias");
  }
  if (responseParser->hasName("prov")) {
  prov = responseParser->getStrByName("prov");
  }
  if (responseParser->hasName("quantity")) {
  quantity = responseParser->getNumByName("quantity");
  }
  if (responseParser->hasName("retail_price_high")) {
  retailPriceHigh = responseParser->getStrByName("retail_price_high");
  }
  if (responseParser->hasName("retail_price_low")) {
  retailPriceLow = responseParser->getStrByName("retail_price_low");
  }
  if (responseParser->hasName("scitem_id")) {
  scitemId = responseParser->getNumByName("scitem_id");
  }
  if (responseParser->hasName("skus")) {
  QList<Parser *> listParser = responseParser->getListObjectParser("skus");
  Parser *parser;
  foreach (parser, listParser) {
    FenxiaoSku tmp;
    tmp.setParser(parser);
    tmp.parseResponse();
    skus.append(tmp);
  }
  }
  if (responseParser->hasName("standard_price")) {
  standardPrice = responseParser->getStrByName("standard_price");
  }
  if (responseParser->hasName("status")) {
  status = responseParser->getStrByName("status");
  }
  if (responseParser->hasName("trade_type")) {
  tradeType = responseParser->getStrByName("trade_type");
  }
  if (responseParser->hasName("upshelf_time")) {
  upshelfTime = responseParser->getDateByName("upshelf_time");
  }

}

